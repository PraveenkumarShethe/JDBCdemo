package org.jdbcBasic.seminar.bean;

public class EmployeeBean {
	
	private String employeeName , employeeAddress , employeeContactNum ;

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public String getEmployeeAddress() {
		return employeeAddress;
	}

	public void setEmployeeAddress(String employeeAddress) {
		this.employeeAddress = employeeAddress;
	}

	public String getEmployeeContactNum() {
		return employeeContactNum;
	}

	public void setEmployeeContactNum(String employeeContactNum) {
		this.employeeContactNum = employeeContactNum;
	}
}
